using UnityEngine;

public class EnemyHealth : MonoBehaviour
{
    public float health;
    // Start is called once before the first execution of Update after the MonoBehaviour is created
    void Start()
    {
        
    }

    // Update is called once per frame
    void Update()
    {
        Debug.Log("Enemy Health: " + health);
        if (health <= 0)
        {
            Destroy(gameObject);
        }
    }
}
